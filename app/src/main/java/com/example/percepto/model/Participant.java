package com.example.percepto.model;

public class Participant {
    private String CURP;
    private String FIRSTNAME;
    private String LASTNAME;
    private int AGE;
    private String GROUP;
    private String TIME;
    private String LEVEL;
    private String IQ;
    private String REEXP;
    private String AVOID;
    private String ACTIVE;
    private String ANX;
    private String DPRS;

    public String getCURP() {
        return CURP;
    }

    public void setCURP(String CURP) {
        this.CURP = CURP;
    }

    public String getFIRSTNAME() {
        return FIRSTNAME;
    }

    public void setFIRSTNAME(String FIRSTNAME) {
        this.FIRSTNAME = FIRSTNAME;
    }

    public String getLASTNAME() {
        return LASTNAME;
    }

    public void setLASTNAME(String LASTNAME) {
        this.LASTNAME = LASTNAME;
    }

    public int getAGE() {
        return AGE;
    }

    public void setAGE(int AGE) {
        this.AGE = AGE;
    }

    public String getGROUP() {
        return GROUP;
    }

    public void setGROUP(String GROUP) {
        this.GROUP = GROUP;
    }

    public String getTIME() {
        return TIME;
    }

    public void setTIME(String TIME) {
        this.TIME = TIME;
    }

    public String getLEVEL() {
        return LEVEL;
    }

    public void setLEVEL(String LEVEL) {
        this.LEVEL = LEVEL;
    }

    public String getIQ() {
        return IQ;
    }

    public void setIQ(String IQ) {
        this.IQ = IQ;
    }

    public String getREEXP() {
        return REEXP;
    }

    public void setREEXP(String REEXP) {
        this.REEXP = REEXP;
    }

    public String getAVOID() {
        return AVOID;
    }

    public void setAVOID(String AVOID) {
        this.AVOID = AVOID;
    }

    public String getACTIVE() {
        return ACTIVE;
    }

    public void setACTIVE(String ACTIVE) {
        this.ACTIVE = ACTIVE;
    }

    public String getANX() {
        return ANX;
    }

    public void setANX(String ANX) {
        this.ANX = ANX;
    }

    public String getDPRS() {
        return DPRS;
    }

    public void setDPRS(String DPRS) {
        this.DPRS = DPRS;
    }


}
